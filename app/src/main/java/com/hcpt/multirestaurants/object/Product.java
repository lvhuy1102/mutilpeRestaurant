package com.hcpt.multirestaurants.object;

public class Product {
    private String food_id;
    private String food_code;
    private String food_name;
    private String food_menus;
    private String food_price;
    private int food_percent_discount;
    private String food_shop;
    private String food_menu;
    private String food_thumbnail;
    private String food_description;
    private String status;
    private String rate;
    private String rate_times;

    public String getFood_id() {
        return food_id;
    }

    public void setFood_id(String food_id) {
        this.food_id = food_id;
    }

    public String getFood_code() {
        return food_code;
    }

    public void setFood_code(String food_code) {
        this.food_code = food_code;
    }

    public String getFood_name() {
        return food_name;
    }

    public void setFood_name(String food_name) {
        this.food_name = food_name;
    }

    public String getFood_menus() {
        return food_menus;
    }

    public void setFood_menus(String food_menus) {
        this.food_menus = food_menus;
    }

    public String getFood_price() {
        return food_price;
    }

    public void setFood_price(String food_price) {
        this.food_price = food_price;
    }

    public int getFood_percent_discount() {
        return food_percent_discount;
    }

    public void setFood_percent_discount(int food_percent_discount) {
        this.food_percent_discount = food_percent_discount;
    }

    public String getFood_shop() {
        return food_shop;
    }

    public void setFood_shop(String food_shop) {
        this.food_shop = food_shop;
    }

    public String getFood_menu() {
        return food_menu;
    }

    public void setFood_menu(String food_menu) {
        this.food_menu = food_menu;
    }

    public String getFood_thumbnail() {
        return food_thumbnail;
    }

    public void setFood_thumbnail(String food_thumbnail) {
        this.food_thumbnail = food_thumbnail;
    }

    public String getFood_description() {
        return food_description;
    }

    public void setFood_description(String food_description) {
        this.food_description = food_description;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getRate_times() {
        return rate_times;
    }

    public void setRate_times(String rate_times) {
        this.rate_times = rate_times;
    }
}
